cc = bb - aa

print(str(cc).split()[0])

# 8. Напишите программу на Python, чтобы получить разницу между
# заданным числом и 17, если число больше 17, верните двойную абсолютную разницу.

def difference(n):
    if n <= 17: return n - 17
    else: return (n - 17)*2

print(difference(37))
print(difference(10))

# 9. Напишите программу на Python, чтобы проверить,
# находится ли число в пределах 100 от 1000 или 2000.

def within_100(n):
    return abs(1000 - n) <= 100 or abs(2000 - n) <= 100


print(within_100(950))
print(within_100(1800))

# 10. Напишите программу на языке Python для расчета суммы
# трех заданных чисел, если значения равны, тогда верните трижды их сумму.

def sum_3(x, y, z):
    s = x + y + z
    if x == y == z: s *= 3
    return s


print(sum_3(1, 2, 3))
print(sum_3(1, 1, 1))
