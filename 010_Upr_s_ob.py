a = input('Введите первую дату в формате гггг.мм.дд ')
b = input('Введите вторую дату в формате гггг.мм.дд ')

a = a.split('.')
b = b.split('.')

aa = datetime.date(int(a[0]), int(a[1]), int(a[2]))
